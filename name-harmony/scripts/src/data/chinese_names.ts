// Helper function to generate full Chinese names
// In Chinese naming convention, family name typically comes first
export function generateChineseFullName(gender: 'male' | 'female' | 'unisex' = 'unisex') {
  // Randomly select a family name
  const randomFamilyNameIndex = Math.floor(Math.random() * CHINESE_FAMILY_NAMES.length);
  const familyName = CHINESE_FAMILY_NAMES[randomFamilyNameIndex].name;
  
  // Filter given names by gender
  const filteredGivenNames = CHINESE_GIVEN_NAMES.filter(name => 
    gender === 'unisex' ? true : name.gender === gender || name.gender === 'unisex'
  );
  
  // Randomly select a given name
  const randomGivenNameIndex = Math.floor(Math.random() * filteredGivenNames.length);
  const givenName = filteredGivenNames[randomGivenNameIndex].name;
  
  // Return formatted full name (family name first)
  return { 
    familyName, 
    givenName, 
    fullName: `${familyName} ${givenName}`,
    pinyinFamilyName: CHINESE_FAMILY_NAMES[randomFamilyNameIndex].pinyin || familyName,
    pinyinGivenName: filteredGivenNames[randomGivenNameIndex].pinyin || givenName,
    meaning: filteredGivenNames[randomGivenNameIndex].meaning || "Unknown"
  };
}

// Generate a two-character given name (very common in Chinese naming)
export function generateTwoCharacterName(gender: 'male' | 'female' | 'unisex' = 'unisex') {
  const filteredGivenNames = CHINESE_GIVEN_NAMES.filter(name => 
    gender === 'unisex' ? true : name.gender === gender || name.gender === 'unisex'
  );
  
  // Select two different characters to create a two-character name
  const firstIndex = Math.floor(Math.random() * filteredGivenNames.length);
  let secondIndex = Math.floor(Math.random() * filteredGivenNames.length);
  
  // Ensure we get two different characters
  while (secondIndex === firstIndex) {
    secondIndex = Math.floor(Math.random() * filteredGivenNames.length);
  }
  
  const firstChar = filteredGivenNames[firstIndex].name;
  const secondChar = filteredGivenNames[secondIndex].name;
  
  // Return the two-character name
  return firstChar + secondChar;
}
